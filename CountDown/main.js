let countdownFn = (text, body) => {
    initNum = parseFloat(initNum - 0.1).toFixed(1)
    if ( initNum >= 0 ) {

        if ( initNum % 1 == 0 ) {
            let randomTextColor = colorArray[Math.floor(Math.random() * colorArray.length)]
            let randomBackgroundColor = colorArray[Math.floor(Math.random() * colorArray.length)]

            while ( randomTextColor == randomBackgroundColor ) {
                randomBackgroundColor = colorArray[Math.floor(Math.random() * colorArray.length)]
            }

            text.style.color = randomTextColor
            body.style.backgroundColor = randomBackgroundColor
        }

        text.innerHTML = parseFloat(text.innerHTML - 0.1).toFixed(1)
    } else {
        text.innerHTML = "Take Off!"
        clearInterval(initCountdown)

        setTimeout(() => {
            initNum = 10;
            text.innerHTML = initNum;
            initCountdown = setInterval(() => {
                countdownFn(text, body)
            }, 100)
            console.log('reset timer')
        }, 3000)
    }
}